from pydantic import BaseModel, Field
from typing import List, Optional, Literal
import uuid

# --- Component Models ---

class ExperimentStep(BaseModel):
    """
    A single step in the experimental protocol.
    """
    step_number: int = Field(..., description="The sequence number of the step.")
    description: str = Field(..., description="A detailed description of what to do in this step.")
    materials_needed: Optional[List[str]] = Field(default=None, description="Materials required for this specific step.")

# --- API Request/Response Models ---

class ExperimentDesignRequest(BaseModel):
    """
    Information provided by the user to initiate an experiment design.
    """
    research_topic: str = Field(
        ...,
        description="The core topic or question the user wants to investigate.",
        examples=["Investigating the catalytic efficiency of novel catalyst A in reaction B"]
    )
    variables: Optional[List[str]] = Field(
        default=None,
        description="Key variables the user has already identified.",
        examples=[["catalyst dosage", "reaction time"]]
    )
    constraints: Optional[List[str]] = Field(
        default=None,
        description="Any constraints on the experiment.",
        examples=["Must use existing equipment"]
    )

class Hypothesis(BaseModel):
    """
    A structured, preliminary hypothesis generated by the LLM.
    """
    hypothesis_text: str = Field(..., description="The clear, testable research hypothesis.")
    context_summary: str = Field(..., description="A brief summary of the knowledge base context used to generate the hypothesis.")

class Critique(BaseModel):
    """
    A structured critique of an experimental design, provided by a reviewer agent.
    """
    suggested_improvements: List[str] = Field(..., description="Specific suggestions to improve the design's rigor, feasibility, or clarity.")
    potential_flaws: List[str] = Field(..., description="Potential flaws, biases, or missing controls in the proposed design.")


class ExperimentDesign(BaseModel):
    """
    The final, structured experimental design.
    This is the core Pydantic model for data validation and API responses.
    """
    title: str = Field(..., description="The title of the experimental plan.")
    hypothesis: str = Field(..., description="The core research hypothesis being tested.")
    methodology: str = Field(..., description="An overview of the primary research methodology.")
    materials: List[str] = Field(..., description="A complete list of all materials and reagents needed.")
    control_group: str = Field(..., description="A description of the control group setup.")
    experimental_group: str = Field(..., description="A description of the experimental group setup.")
    steps: List[ExperimentStep] = Field(..., description="The detailed experimental protocol.")
    data_analysis_plan: str = Field(..., description="The plan for data collection and analysis.")
    potential_risks: Optional[str] = Field(default=None, description="Potential risks and mitigation strategies.")

# --- Session State Model ---

class DesignSession(BaseModel):
    """
    Represents the state of an interactive experiment design session.
    """
    session_id: str = Field(default_factory=lambda: str(uuid.uuid4()), description="Unique identifier for the session.")
    status: Literal["hypothesis_generated", "design_completed"] = Field(
        default="hypothesis_generated", 
        description="The current status of the design session."
    )
    request: ExperimentDesignRequest
    retrieved_context: str
    hypothesis: Hypothesis
    final_design: Optional[ExperimentDesign] = None

class CreateSessionResponse(BaseModel):
    """
    The response returned when a new design session is created.
    """
    session_id: str
    hypothesis: Hypothesis